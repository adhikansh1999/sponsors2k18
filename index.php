			function changeyear(year){
			$.ajax({
				type: "POST",
				url: "ajax_calls.php",
				data:
				{
				  "year":year
				},
				success:function(data)
				    {
				    $('#list_sponsors').html(data);
					$("html, body").animate({ scrollTop: $(window).height() });
				    },
				 error:function(error)
				  {
				  alert('Network error! Please refresh and try again.');
				  }
			});

			}
			function changeyearinit(year)
			{
			  $.ajax({
		      type: "POST",
		      url: "ajax_calls.php",
		      data:
		          {
		          "year":year
		          },
				  success:function(data)
		                {
		                $('#list_sponsors').html(data);
						
		                },
